    private void eliminarCodigoBarras() {
        Long cod = Long.valueOf(codigoTestTxt.getText());
        int a = JOptionPane.showConfirmDialog(this, "Confirme eliminar CÓDIGO BARRAS?\n" + cod, "Atención", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            CodigoBarras cb = null;
            try {
                cb = new CodigosBarraService().getCodigoBarrasByCB(cod);
                new CodigosBarraService().delete(cb);
                JOptionPane.showMessageDialog(this, "Código de Barra ELIMINADO correctamente");
            } catch (Exception ex) {
                Logger.getLogger(IngresarCodigosBarraFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR no borrado");
            }
        }
        limpiarCampos();
    }
