    private void limpiarCampos() {
        filtroTxt.setText("");
        combo.removeAllItems();
        combo.addItem("");
        codigoBarrasTxt.setText("");
        codigoTestTxt.setText("");
        codigoProductoTxt.setText("");
        detalleTxt.setText("");
        filtroTxt.requestFocus();
    }

    private void llenarCombo() {
        combo.removeAllItems();
        combo.addItem("");
        String filtro = filtroTxt.getText();
        productos = null;
        try {
            productos = new ProductoService().getAllProductosOrdenadoByNombre(filtro);
        } catch (Exception ex) {
            Logger.getLogger(IngresarCodigosBarraFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            for (Producto p : productos) {
                combo.addItem(p.getCodigo() + " " + p.getDetalle());
            }
            combo.addFocusListener(null);
            combo.showPopup();
            combo.requestFocus();
        }
    }

    private void buscarProducto() {
        if (!codigoTestTxt.getText().isEmpty()) {
            codigoProductoTxt.setText("");
            detalleTxt.setText("");
            Producto p = null;
            Long cb = Long.valueOf(codigoTestTxt.getText());
            try {
                p = new ProductoService().getProductoByCodigoBarras(cb);
            } catch (Exception ex) {
                //Logger.getLogger(IngresarCodigosBarraFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "NO EXISTE PRODUCTO");
                codigoTestTxt.setText("");
                return;
            }
            if (p != null) {
                codigoProductoTxt.setText(p.getCodigo().toString());
                detalleTxt.setText(p.getDetalle());
                codigoTestTxt.getText();
            }
        }
    }

    private void nuevoProducto() {
        limpiarCampos();
    }

