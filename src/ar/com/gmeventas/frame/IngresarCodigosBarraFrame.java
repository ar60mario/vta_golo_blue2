    private void codigoBarrasTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoBarrasTxtKeyPressed
        if (evt.getKeyCode() == 10) {
            if (!codigoBarrasTxt.getText().isEmpty()) {
                Producto p = null;
                System.out.println(Long.valueOf(codigoBarrasTxt.getText()));
                try {
                    p = new ProductoService().getProductoByCodigoBarras(Long.valueOf(codigoBarrasTxt.getText()));
                } catch (Exception ex) {
                    //Logger.getLogger(IngresarCodigosBarraFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println(p);
                if (p != null) {
                    JOptionPane.showMessageDialog(this, "Codigo existente");
                    return;
                }
//                else{
//                    JOptionPane.showMessageDialog(this, "Codigo disponible");
//                }
                int r = combo.getSelectedIndex();
                CodigoBarras cb = new CodigoBarras();
                cb.setCodigo(Long.valueOf(codigoBarrasTxt.getText()));
                if (r > 0) {
                    p = productos.get(r - 1);
                    cb.setProducto(p);
                    try {
                        new CodigosBarraService().save(cb);
                    } catch (Exception ex) {
                        Logger.getLogger(IngresarCodigosBarraFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    codigoBarrasTxt.setText("");
                    JOptionPane.showMessageDialog(this, "Ingresado Correctamente");
                    codigoBarrasTxt.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_codigoBarrasTxtKeyPressed

