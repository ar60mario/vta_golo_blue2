    private void llenarTabla() {
        try {
            productos = new ProductoService().getProductosEntreCodigos(deCodigo, aCodigo);
        } catch (Exception ex) {
            Logger.getLogger(MostrarTablaProductosACambiarPrecios.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel model = (DefaultTableModel) tablaProductos.getModel();
        if (productos != null) {
            for (Producto prod:productos){
                //System.out.println(prod.getDetalle());
                Object linea[] = new Object[4];
                linea[0] = prod.getCodigo();
                linea[1] = prod.getDetalle();
                linea[2] = prod.getPrecio();
                Double precioConAumento = prod.getPrecio() * (1 + (porcentaje/100));
                linea[3] = rint(precioConAumento*100)/100;
                model.addRow(linea);
            }
            tablaProductos.setModel(model);
        }
    }

    private void grabarPrecios() {
        for (Producto prod: productos) {
            Double precioConAumento = prod.getPrecio() * (1 + (porcentaje/100));
            prod.setPrecio(rint(precioConAumento*100)/100);
            try {
                new ProductoService().updateProducto(prod);
            } catch (Exception ex) {
                Logger.getLogger(MostrarTablaProductosACambiarPrecios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(this, "Productos Actualizados");
        PreciosEntreCodigosFrame pecf = new PreciosEntreCodigosFrame();
        pecf.setVisible(true);
        this.dispose();
    }
