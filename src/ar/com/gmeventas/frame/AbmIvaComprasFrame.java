    private void llenarCombos() {
        Date date = new Date();
        int anio = date.getYear() + 1900;
        int desdeAnio = 2013;
        comboAnio.removeAllItems();
        comboAnio.addItem("");
        for (int x = desdeAnio; x <= anio; x++){
            comboAnio.addItem(x);
        }
        comboMes.removeAllItems();
        comboMes.addItem("");
        comboMes.addItem("ENERO");
        comboMes.addItem("FEBRERO");
        comboMes.addItem("MARZO");
        comboMes.addItem("ABRIL");
        comboMes.addItem("MAYO");
        comboMes.addItem("JUNIO");
        comboMes.addItem("JULIO");
        comboMes.addItem("AGOSTO");
        comboMes.addItem("SETIEMBRE");
        comboMes.addItem("OCTUBRE");
        comboMes.addItem("NOVIEMBRE");
        comboMes.addItem("DICIEMBRE");
    }

    private IvaCompras getIvaComprasSeleccionado() {
        
        int filaSeleccionada = ivaComprasTabla.getSelectedRow();
        
        if(filaSeleccionada != -1){
            IvaCompras ivaCompras = ivaComprasLista.get(filaSeleccionada);
            return ivaCompras;
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Factura de la lista.");
        }        
        return null;
        
    }
