    private void modificar() {
        ivaCompra = null;
        selectRow = ivaComprasTabla.getSelectedRow();
        if (selectRow >-1){
            System.out.println(selectRow);
            ivaCompra = this.getIvaComprasSeleccionado();
            if (ivaCompra != null) {
                ModificarIvaComprasFrame micf = new ModificarIvaComprasFrame(ivaCompra);
                micf.setVisible(true);
                this.dispose();
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una factura para modificar");
        }
    }

