    private void buscar() {
        try {
            fechaDe = sdf.parse(fechaDeTxt.getText());
            fechaAl = sdf.parse(fechaAlTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VerComparativaFacturasEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        try {
            facturas = new IvaVentasService().getFacturasEntreFechas(fechaDe, fechaAl);
        } catch (Exception ex) {
            Logger.getLogger(VerComparativaFacturasEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (facturas != null && !facturas.isEmpty()){
            DefaultTableModel tbl = (DefaultTableModel) tablaFacturas.getModel();
            for(IvaVentas fc:facturas){
                renglones = null;
                Double impuesto2 = 0.00;
                Double gravado2 = 0.00;
                try {
                    renglones = new RenglonFacturaService().getAllRenglonFacturaFromIvaVentas(fc);
                } catch (Exception ex) {
                    Logger.getLogger(VerComparativaFacturasEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(renglones != null && !renglones.isEmpty()){
                    for(RenglonFactura rf:renglones){
                        impuesto2 += rf.getImpuesto();
                        gravado2 += rf.getGravado();
                    }
                }
                Object ob[] = new Object[6];
                ob[0] = fc.getLetra() + " " + fc.getNumeroFactura();
                ob[1] = df.format(fc.getGravado());
                ob[2] = df.format(fc.getImpuesto());
                ob[3] = df.format(gravado2);
                ob[4] = df.format(impuesto2);
                ob[5] = df.format(fc.getTotal());
                tbl.addRow(ob);
            }
            tablaFacturas.setModel(tbl);
        }
    }

