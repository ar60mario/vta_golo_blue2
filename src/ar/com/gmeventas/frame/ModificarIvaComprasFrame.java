    private void limpiarCampos() {
        fechaFacturaTxt.setText("");
        fechaPeriodoTxt.setText("");
        sucursalFacturaTxt.setText("");
        numeroFacturaTxt.setText("");
        razonSocialProveedorTxt.setText("");
        codigoProveedorTxt.setText("");
        gravadoTxt.setText("");
        noGravadoTxt.setText("");
        ivaTxt.setText("");
        totalFacturaTxt.setText("");
        impuestoInternoTxt.setText("");
        otroTxt.setText("");
        percepcionIIBBTxt.setText("");
        percepcionIvaTxt.setText("");
    }

    private void llenarCampos(IvaCompras ivaCompras) {
        fechaPeriodoTxt.setText((sdf.format(ivaCompras.getFechaPeriodo()).toString()));
        fechaFacturaTxt.setText((sdf.format(ivaCompras.getFechaFactura()).toString()));
        sucursalFacturaTxt.setText(ivaCompras.getNumeroSucursal().toString());
        numeroFacturaTxt.setText(ivaCompras.getNumeroFactura().toString());
        if(ivaCompras.getLetra().equals("A")){
            System.out.println(ivaCompras.getLetra());
            comboLetraFactura.setSelectedIndex(0);
        } else {
            if (ivaCompras.getLetra().equals("C")){
                comboLetraFactura.setSelectedIndex(1);
            } else {
                comboLetraFactura.setSelectedIndex(2);
            }
        }
        codigoProveedorTxt.setText(ivaCompras.getProveedor().getCodigo().toString());
        razonSocialProveedorTxt.setText(ivaCompras.getProveedor().getRazonSocial());
        gravadoTxt.setText(df.format(ivaCompras.getGravado()));
        noGravadoTxt.setText(df.format(ivaCompras.getNoGravado()));
        impuestoInternoTxt.setText(df.format(ivaCompras.getImpuestoInterno()));
        percepcionIIBBTxt.setText(df.format(ivaCompras.getPercepcionIiBb()));
        percepcionIvaTxt.setText(df.format(ivaCompras.getPercepcionIva()));
        otroTxt.setText(df.format(ivaCompras.getOtro()));
        ivaTxt.setText(df.format(ivaCompras.getIva()));
        totalFacturaTxt.setText(df.format(ivaCompras.getTotal()));
    }
