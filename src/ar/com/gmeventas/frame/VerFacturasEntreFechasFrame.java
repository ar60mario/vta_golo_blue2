    private void generar(IvaVentas factura) {
        NotaCreditoWebDeFacturaFrame ncwdff = new NotaCreditoWebDeFacturaFrame(factura);
        ncwdff.setVisible(true);
        this.dispose();
    }

    private void buscarFacturas() {
        Date fd = new Date();
        Date fa = new Date();
        try {
            fd = sdf.parse(deTxt.getText());
            fa = sdf.parse(alTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VerFacturasEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        facturas = null;
        try {
            facturas = new IvaVentasService().getSoloFacturasEntreFechas(fd, fa);
        } catch (Exception ex) {
            Logger.getLogger(VerFacturasEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        llenarTabla();
    }

    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        if (facturas != null && !facturas.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (IvaVentas fc : facturas) {
//                System.out.println(fc.getFecha());
//                System.out.println(fc.getLetra());
//                System.out.println(fc.getNumeroSucursal());
//                System.out.println(fc.getNumeroFactura());
//                System.out.println(fc.getCliente());
//                System.out.println(fc.getCliente().getRazonSocial());
//                System.out.println(fc.getTotal());
                Object o[] = new Object[4];
                o[0] = sdf.format(fc.getFecha());
                o[1] = fc.getLetra() + " " + dfs.format(fc.getNumeroSucursal())
                        + "-" + dfn.format(fc.getNumeroFactura());
                o[2] = fc.getCliente().getRazonSocial();
                o[3] = df.format(fc.getTotal());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
