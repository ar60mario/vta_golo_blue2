    private void grabar() {
        if (verificar()) {
            tipoDoc.setCodigo(codigoTxt.getText());
            tipoDoc.setDescripcion(descripcionTxt.getText());
            try {
                new TipoDocService().updateTipoDoc(tipoDoc);
                JOptionPane.showMessageDialog(this, "Tipo Documento Guardado correctamente");
            } catch (Exception ex) {
                Logger.getLogger(ModificarTipoDocForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Guardando");
            }
            MainFrame mf = new MainFrame();
            mf.setVisible(true);
            this.dispose();
        }
    }

    private boolean verificar() {
        if (codigoTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar C贸digo");
            codigoTxt.requestFocus();
            return false;
        }
        if (descripcionTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar Descripci贸n");
            descripcionTxt.requestFocus();
            return false;
        }
        return true;
    }

    private void llenarCampos() {
        codigoTxt.setText(tipoDoc.getCodigo());
        descripcionTxt.setText(tipoDoc.getDescripcion());
        descripcionTxt.requestFocus();
    }
